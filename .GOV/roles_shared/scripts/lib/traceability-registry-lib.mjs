// Work Packet Traceability Registry — JSON-primary authority library.
//
// JSON is authority: `.GOV/roles_shared/records/wp_traceability_registry.json`.
// Markdown is a generated projection: `.GOV/roles_shared/records/WP_TRACEABILITY_REGISTRY.md`.
// Readers (build-order-sync, packet-truth-check, etc.) continue parsing the
// projection .md; the projection is regenerated from .json on every write so
// the two stay deterministically in sync.
//
// Only `wp-traceability-set.mjs` (and the one-shot migration) writes the .json.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, "../../../../");
const GOV_ROOT = path.join(REPO_ROOT, ".GOV");
const DEFAULT_JSON_PATH = path.join(
  GOV_ROOT,
  "roles_shared",
  "records",
  "wp_traceability_registry.json",
);
const DEFAULT_MD_PATH = path.join(
  GOV_ROOT,
  "roles_shared",
  "records",
  "WP_TRACEABILITY_REGISTRY.md",
);

export const REGISTRY_SCHEMA_ID = "hsk.wp_traceability_registry@1";
export const REGISTRY_SCHEMA_VERSION = "wp_traceability_registry_v1";
export const REGISTRY_DEFAULT_JSON_PATH = DEFAULT_JSON_PATH;
export const REGISTRY_DEFAULT_MD_PATH = DEFAULT_MD_PATH;

const ENTRY_HEADER = "| Base WP ID | Active Packet | Task Board | Notes |";
const ENTRY_DIVIDER = "|-----------:|---------------|------------|-------|";
const SUPERSEDED_HEADER =
  "| BASE_WP_ID | SUPERSEDED_PACKET_OR_STUB | TASK_BOARD | Replacement / Notes |";
const SUPERSEDED_DIVIDER =
  "|------------|---------------------------|------------|---------------------|";
const LINEAGE_HEADER =
  "| Base WP ID | Historical Failed Packet | Historical Classification | Live Smoketest Status | Active Recovery Packet | Driver Audit | Latest Smoketest Review |";
const LINEAGE_DIVIDER =
  "|-----------:|--------------------------|---------------------------|----------------------|------------------------|--------------|-------------------------|";

const FROZEN_PROSE_INTRO = `# Work Packet Traceability Registry (SSoT)

> **GENERATED PROJECTION — DO NOT HAND-EDIT.**
> Authority lives in \`.GOV/roles_shared/records/wp_traceability_registry.json\`.
> This Markdown projection is regenerated by \`wp-traceability-set.mjs\` and the
> traceability migration helper. Edits to this file will be overwritten.
> Operator-readable surface only; consumers may still parse the table rows
> below for backward compatibility while [MD-ELIM-PHASE-2+] consumer migration
> proceeds (see \`.GOV/roles_shared/records/REFACTOR-MD-ELIMINATION-V1.json\`).

**Purpose**
Handshake uses Work Packets (WPs) as execution units, but the Master Spec Main Body must remain stable and WP-free. This registry is the **single source of truth** for mapping:

- **Base WP IDs** (stable planning identifiers used by Roadmap/Task Board), to
- **Active work packet files** (the concrete packet to implement/validate), including any \`-vN\` revisions.

This avoids retroactively embedding WP IDs into the Master Spec and prevents drift when packets are revised (v2/v3/v4) after audits.

Packet lifecycle truth lives in the packet or stub metadata itself. This registry only answers: "which packet is active for this Base WP?" Status surfaces elsewhere must reconcile to the active packet header.

---

## Definitions

- **Base WP ID**: The stable identifier for a scope of work, formatted \`WP-{phase}-{name}\` (e.g., \`WP-1-Workflow-Engine\`).
  - Base IDs **do not** include packet revision suffixes.
  - Base IDs are the preferred identifiers for Roadmap pointers and Task Board tracking.

- **Packet Revision**: A revised work packet for the same Base WP, named \`WP-{phase}-{name}-v{N}\` (e.g., \`WP-1-Workflow-Engine-v4\`).
  - Naming rule is governed by Handshake Codex v1.4 \`[CX-580C]\` (no date/time stamps; use \`-vN\`).
  - **Legacy exception:** historical packets may contain date stamps (e.g., \`-20251228\`). Do not create new date-stamped packet IDs; convert future revisions to \`-vN\`.

- **Active Packet**: The single packet file that is currently authoritative for implementation/validation of a Base WP.

- **Historical Failed Packet**: A prior packet revision whose earlier closure or PASS claim is no longer accepted as trustworthy under current audit or governance law.

- **Live Smoketest Baseline**: A historical failed packet that must still remain visible as the comparison baseline for a current recovery or proof run. This does not reopen execution on the historical packet itself.

- **Superseded Packet**: A prior packet revision that is no longer authoritative. Superseded packets are immutable history; do not catch them up.
  - Deterministic rule: if two or more official packets share the same \`BASE_WP_ID\`, the packet listed here is the active one; all others are older history and must appear under \`## Superseded (Archive)\` on \`.GOV/roles_shared/records/TASK_BOARD.md\`.

---

## Workflow (Deterministic)

1. **Roadmap points to Base WP IDs** (not packet revisions).
2. **Task Board tracks projections of packet truth** (Base IDs and/or packet revisions). This registry resolves the Base WP -> Active Packet mapping when \`-vN\` revisions exist.
3. **work packets live in** \`.GOV/task_packets/\`. **Stubs live in** \`.GOV/task_packets/stubs/\`.
4. If a packet must change due to audit/spec drift:
   - Create a **new packet revision** \`...-v{N}\` (do not edit locked history).
   - Mark the older packet as **Superseded** on \`.GOV/roles_shared/records/TASK_BOARD.md\`.
   - Update this registry to point the Base WP to the new Active Packet.
5. If a historical packet remains relevant as a live smoketest baseline after being judged incomplete or false-closed:
   - keep the new recovery packet as the **Active Packet**
   - keep the historical packet under \`## Superseded (Archive)\` on the Task Board
   - also record the pair under \`## Historical Failure + Live Smoketest Lineage\` so failed historical closure and live smoketest lineage remain separate modeled truths

**Registry update is mandatory whenever more than one packet exists for the same Base WP.** If mapping is missing or ambiguous, the WP is governance-blocked until resolved.

### How to use with \`just\` / validation scripts (frictionless rule)

- When running \`just phase-check <PHASE> <WP_ID> <ROLE>\`, validator scripts, etc., use the **Active Packet WP_ID** (the filename stem), not the Base WP ID.
  - Example: if Active Packet is \`.GOV/task_packets/WP-1-Workflow-Engine-v4.md\`, run \`just phase-check STARTUP WP-1-Workflow-Engine-v4 CODER\`.
- If the Active Packet is a stub under \`.GOV/task_packets/stubs/\`, it is **not executable**: activate it first (Technical Refinement Block -> USER_SIGNATURE -> create official work packet).

---

## Registry (Phase 1)

Format:
- **Base WP ID**: stable
- **Active Packet**: authoritative file path
- **Task Board**: expected projection target derived from the active packet header
- **Notes**: supersedes history / special cases

`;

const FROZEN_PROSE_SUPERSEDED_INTRO = `## Superseded Activation History

Use this section to keep older active packets / stubs visible as superseded once they've been folded into a newer revision. The replacement / fold reason and the active successor live in the Replacement / Notes column.

`;

const FROZEN_PROSE_LINEAGE_INTRO = `## Historical Failure + Live Smoketest Lineage

Use this section when a historical packet must remain visible as a failed closure and as the baseline for a current or future smoketest recovery line. The active recovery packet still lives in the main registry above.

`;

function readTextOrThrow(absPath) {
  return fs.readFileSync(absPath, "utf8");
}

function parsePipeRow(line) {
  if (!line.trim().startsWith("|")) return null;
  return line.split("|").slice(1, -1).map((cell) => cell.trim());
}

// Parse the legacy .md format into a registry object. Used once for migration
// and exported for tests / verification. Three table modes:
//   - "entries"            (main active registry)
//   - "superseded_history" (Superseded Activation History table)
//   - "lineage"            (Historical Failure + Live Smoketest Lineage)
export function parseLegacyMarkdown(mdText = "") {
  const lines = String(mdText).split(/\r?\n/);
  const entries = [];
  const supersededActivationHistory = [];
  const historicalLineage = [];
  let mode = "none";
  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    const trimmed = line.trim();

    if (trimmed === ENTRY_HEADER) {
      mode = "entries";
      continue;
    }
    if (trimmed === SUPERSEDED_HEADER) {
      mode = "superseded_history";
      continue;
    }
    if (trimmed === LINEAGE_HEADER) {
      mode = "lineage";
      continue;
    }
    if (trimmed.startsWith("##")) {
      mode = "none";
      continue;
    }
    if (trimmed === "") {
      continue; // blank lines between rows in the same table are allowed
    }
    if (!trimmed.startsWith("|")) {
      mode = "none";
      continue;
    }
    if (trimmed.startsWith("|--") || trimmed.startsWith("|---")) continue;

    const parts = parsePipeRow(line);
    if (!parts || parts.length < 2) continue;
    const first = parts[0];
    if (!first.startsWith("WP-")) continue;

    if (mode === "entries") {
      entries.push({
        base_wp_id: first,
        active_packet_path: parts[1] || "",
        task_board_projection: parts[2] || "",
        notes: parts.slice(3).join(" | ") || "",
      });
    } else if (mode === "superseded_history") {
      supersededActivationHistory.push({
        base_wp_id: first,
        superseded_packet_or_stub: parts[1] || "",
        task_board: parts[2] || "",
        replacement_notes: parts.slice(3).join(" | ") || "",
      });
    } else if (mode === "lineage") {
      historicalLineage.push({
        base_wp_id: first,
        historical_failed_packet: parts[1] || "",
        historical_classification: parts[2] || "",
        live_smoketest_status: parts[3] || "",
        active_recovery_packet: parts[4] || "",
        driver_audit: parts[5] || "",
        latest_smoketest_review: parts[6] || "",
      });
    }
  }
  return { entries, supersededActivationHistory, historicalLineage };
}

function nowIsoUtc() {
  return new Date().toISOString();
}

export function emptyRegistry() {
  return {
    schema_id: REGISTRY_SCHEMA_ID,
    schema_version: REGISTRY_SCHEMA_VERSION,
    authority_surface: "MACHINE_CONTRACT",
    rendered_md_projection_path:
      ".GOV/roles_shared/records/WP_TRACEABILITY_REGISTRY.md",
    rendered_md_projection_policy: "GENERATED_FROM_JSON_DO_NOT_HAND_EDIT",
    updated_at_utc: nowIsoUtc(),
    entries: [],
    superseded_activation_history: [],
    historical_lineage: [],
  };
}

// Load .json from disk. Throws if not present.
export function loadRegistryJson(jsonPath = DEFAULT_JSON_PATH) {
  const raw = readTextOrThrow(jsonPath);
  const parsed = JSON.parse(raw);
  if (parsed.schema_id !== REGISTRY_SCHEMA_ID) {
    throw new Error(
      `Unexpected schema_id in ${jsonPath}: ${parsed.schema_id}`,
    );
  }
  if (!Array.isArray(parsed.entries)) {
    throw new Error(`Missing entries[] in ${jsonPath}`);
  }
  if (!Array.isArray(parsed.historical_lineage)) {
    parsed.historical_lineage = [];
  }
  if (!Array.isArray(parsed.superseded_activation_history)) {
    parsed.superseded_activation_history = [];
  }
  return parsed;
}

// Atomic write of .json.
export function writeRegistryJson(
  jsonPath = DEFAULT_JSON_PATH,
  registry = emptyRegistry(),
) {
  const next = {
    ...registry,
    schema_id: REGISTRY_SCHEMA_ID,
    schema_version: REGISTRY_SCHEMA_VERSION,
    authority_surface: "MACHINE_CONTRACT",
    rendered_md_projection_path:
      ".GOV/roles_shared/records/WP_TRACEABILITY_REGISTRY.md",
    rendered_md_projection_policy: "GENERATED_FROM_JSON_DO_NOT_HAND_EDIT",
    updated_at_utc: nowIsoUtc(),
    entries: Array.isArray(registry.entries) ? registry.entries : [],
    historical_lineage: Array.isArray(registry.historical_lineage)
      ? registry.historical_lineage
      : [],
    superseded_activation_history: Array.isArray(registry.superseded_activation_history)
      ? registry.superseded_activation_history
      : [],
  };
  fs.writeFileSync(jsonPath, `${JSON.stringify(next, null, 2)}\n`, "utf8");
  return next;
}

function renderEntryRow(entry) {
  return `| ${entry.base_wp_id} | ${entry.active_packet_path} | ${
    entry.task_board_projection || "TBD"
  } | ${entry.notes || ""} |`;
}

function renderLineageRow(row) {
  return `| ${row.base_wp_id} | ${row.historical_failed_packet} | ${row.historical_classification} | ${row.live_smoketest_status} | ${row.active_recovery_packet} | ${row.driver_audit} | ${row.latest_smoketest_review} |`;
}

function renderSupersededRow(row) {
  return `| ${row.base_wp_id} | ${row.superseded_packet_or_stub} | ${row.task_board} | ${row.replacement_notes || ""} |`;
}

// Render the full markdown projection from a registry object.
export function renderRegistryMarkdown(registry) {
  const parts = [FROZEN_PROSE_INTRO, ENTRY_HEADER, ENTRY_DIVIDER];
  for (const entry of registry.entries) parts.push(renderEntryRow(entry));
  parts.push("");
  parts.push(FROZEN_PROSE_SUPERSEDED_INTRO);
  parts.push(SUPERSEDED_HEADER);
  parts.push(SUPERSEDED_DIVIDER);
  parts.push("");
  for (const row of registry.superseded_activation_history || []) {
    parts.push(renderSupersededRow(row));
  }
  parts.push("");
  parts.push(FROZEN_PROSE_LINEAGE_INTRO);
  parts.push(LINEAGE_HEADER);
  parts.push(LINEAGE_DIVIDER);
  for (const row of registry.historical_lineage) parts.push(renderLineageRow(row));
  parts.push("");
  return parts.join("\n");
}

// Write the markdown projection to disk.
export function writeRegistryMarkdown(
  mdPath = DEFAULT_MD_PATH,
  registry = emptyRegistry(),
) {
  const text = renderRegistryMarkdown(registry);
  fs.writeFileSync(mdPath, text, "utf8");
  return text;
}

// Convenience: write JSON + regenerate MD in one call. Single-writer entry
// point used by wp-traceability-set.mjs and the migration helper.
export function persistRegistry(
  registry,
  { jsonPath = DEFAULT_JSON_PATH, mdPath = DEFAULT_MD_PATH } = {},
) {
  const written = writeRegistryJson(jsonPath, registry);
  writeRegistryMarkdown(mdPath, written);
  return written;
}

// Find an entry by base_wp_id (case-sensitive exact match).
export function findEntry(registry, baseWpId) {
  return (registry.entries || []).find((entry) => entry.base_wp_id === baseWpId) || null;
}

// Insert or update an entry; preserves existing task_board_projection + notes
// when not overridden.
export function upsertEntry(registry, newEntry) {
  const existingIndex = (registry.entries || []).findIndex(
    (entry) => entry.base_wp_id === newEntry.base_wp_id,
  );
  if (existingIndex >= 0) {
    const existing = registry.entries[existingIndex];
    registry.entries[existingIndex] = {
      base_wp_id: newEntry.base_wp_id,
      active_packet_path:
        newEntry.active_packet_path || existing.active_packet_path,
      task_board_projection:
        newEntry.task_board_projection !== undefined
          ? newEntry.task_board_projection
          : existing.task_board_projection,
      notes: newEntry.notes !== undefined ? newEntry.notes : existing.notes,
    };
  } else {
    registry.entries.push({
      base_wp_id: newEntry.base_wp_id,
      active_packet_path: newEntry.active_packet_path,
      task_board_projection: newEntry.task_board_projection || "TBD",
      notes: newEntry.notes || "",
    });
  }
  return registry;
}

// One-shot migration: if .json doesn't exist, parse the legacy .md and write
// the .json + regenerate the projection. Returns the registry after migration.
// Idempotent: if .json already exists, returns the loaded registry unchanged.
export function migrateRegistryFromMarkdownIfNeeded({
  jsonPath = DEFAULT_JSON_PATH,
  mdPath = DEFAULT_MD_PATH,
} = {}) {
  if (fs.existsSync(jsonPath)) {
    return loadRegistryJson(jsonPath);
  }
  if (!fs.existsSync(mdPath)) {
    throw new Error(
      `Cannot migrate: neither ${jsonPath} nor ${mdPath} exists`,
    );
  }
  const mdText = readTextOrThrow(mdPath);
  const parsed = parseLegacyMarkdown(mdText);
  const registry = {
    ...emptyRegistry(),
    entries: parsed.entries,
    superseded_activation_history: parsed.supersededActivationHistory || [],
    historical_lineage: parsed.historicalLineage,
  };
  return persistRegistry(registry, { jsonPath, mdPath });
}
